/**
 * Get a deterministic message for a guest based on theme and guest ID
 * @param {string} themeId - The theme identifier
 * @param {string} guestId - The guest identifier
 * @returns {string} - A themed message
 */
function getMessageForGuest(themeId, guestId) {
    const messages = THEME_MESSAGES[themeId] || THEME_MESSAGES.classic;

    // Simple hash function to get consistent index based on guest ID
    let hash = 0;
    for (let i = 0; i < guestId.length; i++) {
        hash = ((hash << 5) - hash) + guestId.charCodeAt(i);
        hash = hash & hash; // Convert to 32bit integer
    }

    const index = Math.abs(hash) % messages.length;
    return messages[index];
}

/**
 * Get all messages for a specific theme
 * @param {string} themeId - The theme identifier
 * @returns {array} - Array of messages for the theme
 */
function getThemeMessages(themeId) {
    return THEME_MESSAGES[themeId] || THEME_MESSAGES.classic;
}

